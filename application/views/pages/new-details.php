<p>Cung cấp sức mạnh cho xe là động cơ xăng SkyActiv-G dung tích 2.5L cho công suất mạnh mẽ 188 mã lực tại 6.000 vòng/phút và mô-men xoắn 252 Nm tại 4.000 vòng/phút, kết hợp với hộp số tự động 6 cấp và hệ dẫn động AWD (phiên bản Premium AWD). Chưa hết, CX-8 còn có thêm chức năng ngắt động cơ i-Stop, chế độ lái thể thao cùng công nghệ kiểm soát gia tốc G-Vectoring Control (GVC)… giúp xe tăng tốc nhanh nhạy, vận hành êm ái và có mức tiêu hao nhiên liệu thấp.</p>

<p>Đáng chú ý, bán kính vòng quay của Mazda CX-8 chỉ là 5,8m, tương đương những mẫu xe 5+2, giúp xe vận hành ổn định và dễ dàng xoay sở hơn khi di chuyển trong đô thị.</p>

<p>Có thể thấy, trong bối cảnh áp lực cạnh tranh của phân khúc SUV 7 chỗ ngày càng lớn, Mazda CX-8 vẫn dễ dàng chinh phục khách hàng nhờ những lợi thế nổi bật. Không chỉ sở hữu diện mạo sang trọng, lịch lãm, mẫu SUV cao cấp của Mazda còn được cung cấp nhiều trang bị, tiện nghi đẳng cấp vượt trội so với các mẫu xe tầm giá 1 tỷ đồng, trở thành một trong những lựa chọn SUV 7 chỗ hàng đầu tại thị trường Việt Nam hiện nay.</p>
